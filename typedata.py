# macam-macam type data
 
#1 string : text
Nama = "Ahmad Firdaus"

#2 integer : bilangan bulat
umur = 18

#3 float : bilangan desimal
beratbadan = 48.9

#4 List : multiple data
hobi = ['memancing','berenang','membaca']

#5 tuple : multiple data
makananfavorit = ('bakso','nasi tempong','mie ayam')

#6 set : multiple data
namasaudara = {'andi','ani','ana'}

#6 dictionary :
anakpertama = {'anak pertama': 'andi', 'umur':'20'}

#untuk menampilkan type data pada variable 
print("type datanya = ",type(Nama))
print("type datanya = ",type(umur))
print("type datanya = ",type(beratbadan))
print("type datanya = ",type(hobi))
print("type datanya = ",type(makananfavorit))
print("type datanya = ",type(namasaudara))
print("type datanya = ",type(anakpertama))
